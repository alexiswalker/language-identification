    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {                                          
        try {
            int codigo = Integer.parseInt(txtCodigo.getText());
            String nome = txtNome.getText();
            String telefone = txtfTelefone.getText();
            Cliente cliente = new Cliente(codigo, nome, telefone);
            if (clientes.contains(cliente)) {
                JOptionPane.showMessageDialog(null, "O c칩digo informado j치 est치 cadastrado");
            } else {
                clientes.add(cliente);
                JOptionPane.showMessageDialog(null, "Cliente salvo com sucesso!");
                limparCampos();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERRO: " + e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }                                         

    private void btnExibirActionPerformed(java.awt.event.ActionEvent evt) {                                          
        String client = "";
        for (Cliente c : clientes) {
            client += c.getInformacao();
        }

        JOptionPane.showMessageDialog(null, client);
    }                                         

    public void limparCampos() {
        txtCodigo.setText(null);
        txtCodigo.requestFocus();
        txtNome.setText(null);
        txtfTelefone.setText(null);
    }

