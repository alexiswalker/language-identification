function main()
  LaunchScript("megamenulib.lua")
  ImportScript("megamenulib.lua")
  TextPrintString("Clarity Menu", 3, 1)
  WaitSkippable(3000)
  TextPrintString("Created by:\nUnknn0wnsoldier1138", 3, 1)
  WaitSkippable(3000)
  TextPrintString("Beta tested by:\nShrimp", 3, 1)
  WaitSkippable(3000)
  TextPrintString('', 0, 1)
  TextPrintString(crest.."Main Menu"..crest, 3, 2)
  MEGAMENURUNNING = true
  while MEGAMENURUNNING do
    PedClearObjectives(gPlayer)
    monitor_menus()
	if IsButtonBeingPressed(3, 0) or IsButtonBeingPressed(2, 0) then
	  SoundPlay2D("ButtonUp")
	end
    Wait(0)
  end
end

function monitor_menus()
  if menu == "main" then
    menu_main()
  elseif menu == "Model Swapper" then
    menu_model_swapper()
  elseif menu == "Style Changer" then
    menu_style_changer()
  elseif menu == "Ped Spawner" then
    menu_ped_spawner()
  end
end

function menu_main()
  if IsButtonPressed(0, 0) then
    Wait(100)
	SoundPlay2D(ButtonLeft)
	mmi = mmi - 1
	if mmi < 1 then
	  mmi = table.getn(mainmenu)
	end
	TextPrintString(crest.."Main Menu"..crest.."\n<"..mmi..". "..mainmenu[mmi], 3, 2)
  elseif IsButtonPressed(1, 0) then
    Wait(100)
	SoundPlay2D(ButtonRight)
	mmi = mmi + 1
	if mmi > table.getn(mainmenu) then
	  mmi = 1
	end
	TextPrintString(crest.."Main Menu"..crest.."\n"..mmi..". "..mainmenu[mmi]..">", 3, 2)
  elseif IsButtonBeingPressed(3, 0) then
    SoundPlay2D(ButtonUp)
	menu = mainmenu[mmi]
	TextPrintString(crest..mainmenu[mmi]..crest, 3, 2)
  end
end

