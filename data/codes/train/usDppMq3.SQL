CREATE TABLE ABASTECIMENTOS (
    CODIGO              INTEGER NOT NULL,
    CODTIPOCOMBUSTIVEL  INTEGER,
    CODEQUIPAMENTO      INTEGER,
    CODFORNECEDOR       INTEGER,
    QTD                 FLOAT,
    DATAABASTECIMENTO   DATE,
    CODFUNCIONARIO      INTEGER,
    KMHR                FLOAT,
    OBS                 VARCHAR(255),
    VLRUNIT             NUMERIC(9,2),
    TOTAL               NUMERIC(9,2),
    MARCADA             INTEGER,
    CODPLANOCONTA       INTEGER,
    CODSUBPLANOCONTA    INTEGER,
    DOCUMENTO           VARCHAR(10),
    STATUS              CHAR(1)
);

CREATE TABLE MANUTENCOES (
    CODIGO           INTEGER NOT NULL,
    DESCRICAO        VARCHAR(200),
    CODFORNECEDOR    INTEGER,
    TOTAL            NUMERIC(9,2),
    CODCONDICAOPGTO  INTEGER,
    CODPLANOCONTA    INTEGER,
    CODEQUIPAMENTO   INTEGER,
    OBS              VARCHAR(300),
    STATUS           CHAR(1),
    DATAABERTURA     DATE,
    VALORSERVICOS    NUMERIC(9,2),
    CODSUBPLANO      INTEGER,
    TIPO             INTEGER,
    NUMERONF         VARCHAR(10)
);

