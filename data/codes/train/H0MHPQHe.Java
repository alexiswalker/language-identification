    @Override
    public void paintComponent(Graphics g) {
        for (int y = 0; y < 360; y +=60) {
            int i = 0;
            for (int x = 0; x < 300; x +=60) {
                printKot(g,x,y);
                printLetter(g,x,y,i);
                i++;
            }
        }
    }

    public Raster(String inputwoord) {
        this.inputWoord = inputwoord;
    }
    
    /**
     *
     * @param g Graphics
     * @param x Een coördinaat op de x-as
     * @param y Een coördinaat op de y-as
     */
    public void printKot(Graphics g, int x, int y) {
                g.drawRect(x, y, 60, 60); 
    }
   
    /**
     *
     * @param g Graphics
     * @param x Een coördinaat op de x-as
     * @param y Een coördinaat op de y-as
     * @param i Een teller om alle letter van het woord te doorlopen
     */
    public void printLetter(Graphics g, int x, int y, int i) {
        g.setFont(new Font("TimesRoman", Font.BOLD, 40));   
        g.setColor(Color.red);
        g.drawString(Character.toString(inputWoord.charAt(i)), 15 + x, 40 + y);
        g.setColor(Color.black);
    }
    
